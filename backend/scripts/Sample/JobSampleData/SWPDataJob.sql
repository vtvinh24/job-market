-- Insert corresponding data entries into job_log table
INSERT INTO job_log (job_id, job_log_type, job_log_time)
SELECT job_id, 'create', DATEADD(DAY, -FLOOR(1 + (RAND() * 365)), GETDATE())
FROM job
ORDER BY job_id DESC
