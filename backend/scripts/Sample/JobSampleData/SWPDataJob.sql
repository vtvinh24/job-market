-- Insert corresponding data entries into job_history table
INSERT INTO job_history (user_id, job_id, job_status)
SELECT (ROW_NUMBER() OVER (ORDER BY job_id) % 10) + 1, job_id, CASE WHEN job_id % 2 = 0 THEN 'ongoing' ELSE 'pending' END
FROM job
ORDER BY job_id DESC
OFFSET 0 ROWS FETCH NEXT 100 ROWS ONLY;

